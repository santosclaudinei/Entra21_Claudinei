# 2) Crie uma lista com 10 números qualquer.
# 
# Usando as funções da lista responda:
# 
# Quantos itens tem a lista?
# Qual é o número maior?
# Qual é o número menor?
# Qual é o resultado da soma da lista?

lista = [10, 33, 45, 22, 66, 99, 15, 30, 87, 93]                # Cria uma lista com dez elementos
print(f'O numero de itens na lista é {len(lista)}')             # Retorna o numeros de elementos presentes na lista
print(f'O maior numero da lista é {max(lista)}')                # Retorna o maior valor da lista
print(f'O menor numero da lista é {min(lista)}')                # Retorna o menor valor da lista
print(f'A soma de todos os numeros da lista é {sum(lista)}')    # Retorna a soma de todos os elementos da lista.