# Escreva um programa que peça o peso (float) de 3 pessoas e motre na tela a soma de todos os pesos. 

peso1 = float(input("Digite o peso da primeira pessoa: "))
peso2 = float(input("Digite o peso da segunda pessoa: "))
peso3 = float(input("Digite o peso da terceira pessoa: "))
soma = peso1 + peso2 + peso3
print('O somatorio de todos os pesos é de:',soma,'KG')



