# 1) Crie uma lista vazia.
# Adicione as 3 palavras "Abacate" , "Dia bonito",  "Maria Tereza" , usando o .append() 
# em cada uma destas palavras separadamente.

lista = []                                                  # Cria uma lista sem elementos
lista.append("Abacate")                                     # Adciona o elemento Abacate a lista
lista.append("Dia bonito")                                  # Adciona o elemento Dia bonito a lista
lista.append("Maria Tereza")                                # Adciona o elemento Maria Tereza a lista
print(f'Os elementos presentes na lista são: {lista}')      # Imprime a lista final com seus elementos.
